const seed = async () => {
  console.log('🌱 Seeding database...');

  const user = await seedUser();
  if (!user) {
    console.log('⚠️ Unable to create user. Seeding stopped');
    return;
  }
  await db.insert(trades).values(getNewTrades(user.id));
  console.log('✅ Seed completed');
};

